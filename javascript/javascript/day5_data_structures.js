// Day 5 - JavaScript Data Structures

// Array of students
let students = ["Ravi", "Priya", "Kalyani"];
console.log("Students:", students);

// Add a student
students.push("Anil");
console.log("Updated Students:", students);

// Object with student marks
let marks = {Ravi: 85, Priya: 90, Kalyani: 78};
console.log("Marks object:", marks);

// Add/update marks
marks["Anil"] = 88;
console.log("Updated marks:", marks);
// Day 2 - JavaScript Conditions & Operators

// Adult or Child
let age = 20;
if (age >= 18) {
    console.log("Adult");
} else {
    console.log("Child");
}

// Grade system
let marks = 85;
if (marks >= 90) {
    console.log("Grade: A");
} else if (marks >= 75) {
    console.log("Grade: B");
} else if (marks >= 60) {
    console.log("Grade: C");
} else {
    console.log("Grade: F");
}

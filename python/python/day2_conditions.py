# Day 2 - Python Conditions & Operators

# Even or Odd
number = 7
if number % 2 == 0:
    print(number, "is Even")
else:
    print(number, "is Odd")

# Grade system
marks = 85
if marks >= 90:
    print("Grade: A")
elif marks >= 75:
    print("Grade: B")
elif marks >= 60:
    print("Grade: C")
else:
    print("Grade: F")
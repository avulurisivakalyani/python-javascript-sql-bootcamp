# Day 5 - Python Data Structures

# List of students
students = ["Ravi", "Priya", "Kalyani"]
print("Students:", students)

# Add a student
students.append("Anil")
print("Updated Students:", students)

# Dictionary with student marks
marks = {"Ravi": 85, "Priya": 90, "Kalyani": 78}
print("Marks dictionary:", marks)

# Access and update
marks["Anil"] = 88
print("Updated marks:", marks)